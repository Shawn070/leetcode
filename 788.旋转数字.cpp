class Solution {
public:
    int rotatedDigits(int N) {
        vector<int> sign = {1, 1, 2, 0, 0, 2, 2, 0, 1, 2};
        int res = 0;
        for (int i=1; i <= N; i++) {
            // si ???
            // sum ????
            // n ???
            int si = 0, sum = 0, time = 0, tmp = i;
            while (tmp) {
                si = sign[tmp % 10];
                if (si == 0) {
                    sum = -1;
                    break;
                } 
                sum += si;
                time++;
                tmp /= 10;
            }
            if (sum != -1 && sum != time)
                res++;
        }
        
        return res;
    }
};

