/**
 * Definition for singly-linked list.
 * struct ListNode {
 *     int val;
 *     ListNode *next;
 *     ListNode(int x) : val(x), next(NULL) {}
 * };
 */

class Solution {
public:
    ListNode *getIntersectionNode(ListNode *headA, ListNode *headB) {
/**
??????, ???????????????????????, ???????
???(???????????????)
??????????????, ??????, ???????????????
headA: x1 + y
headB: x2 + y
x1+y+x2 = x2+y+x1
**/
        ListNode* a = headA,* b = headB;
// ?????????pA?pB?????????????????, ???????
// ??pA?pB???????, ??????? NULL==NULL
        while (a != b) {
            a = (a == NULL ? headA : a->next);
            b = (b == NULL ? headB : b->next);
        }
        return a;
    }
};

